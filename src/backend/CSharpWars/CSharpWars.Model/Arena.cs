namespace CSharpWars.Model
{
    /// <summary>
    /// Model class representing the arena dimensions.
    /// </summary>
    public class Arena
    {
        /// <summary>
        /// The width of the arena in number of tiles.
        /// </summary>
        public int Width { get; set; }

        /// <summary>
        /// The height of the arena in number of tiles.
        /// </summary>
        public int Height { get; set; }
    }
}